/* pmm_init - initialize the physical memory management */
static void
page_init(void) {
    // 线性地址0x8000 + KERNBASE对应着物理地址0x8000
    // e820map 储存着BIOS探索的内存布局
    struct e820map *memmap = (struct e820map *)(0x8000 + KERNBASE);
    uint64_t maxpa = 0;

    cprintf("e820map:\n");
    int i;
    for (i = 0; i < memmap->nr_map; i ++) {
        uint64_t begin = memmap->map[i].addr, end = begin + memmap->map[i].size;
        cprintf("  memory: %08llx, [%08llx, %08llx], type = %d.\n",
                memmap->map[i].size, begin, end - 1, memmap->map[i].type);
        // type==01h才是系统可用的
        // 然后检查这块内存的位置，起始位置需要小于KMEMSIZE，因为内核在物理内存地址为0~KMEMSIZE，起始位置太高了就用不了
        // 终止位置取一个最大的
        if (memmap->map[i].type == E820_ARM) {
            if (maxpa < end && begin < KMEMSIZE) {
                maxpa = end;
            }
        }
    }
    // 如果实际内存大于896MB，我们也只用896MB，因为这个实验里，人为定义了物理内存的前896MB作为内核使用
    if (maxpa > KMEMSIZE) {
        maxpa = KMEMSIZE;
    }

    extern char end[];

    npage = maxpa / PGSIZE;
    // BSS段结束处再往上对齐一下，作为“管理空闲空间”的区域，装着所有的npage个Page
    pages = (struct Page *)ROUNDUP((void *)end, PGSIZE);

    // 先把所有的页设置为“保留”
    for (i = 0; i < npage; i ++) {
        SetPageReserved(pages + i);
    }

    // 空闲内存空间的起始地址，这以上的空间可以被分配和释放，以下的空间是内核和页表项
    uintptr_t freemem = PADDR((uintptr_t)pages + sizeof(struct Page) * npage);

    for (i = 0; i < memmap->nr_map; i ++) {
        // 遍历每一个可用的物理内存块（BIOS给我的，每一块都是物理连续的）
        uint64_t begin = memmap->map[i].addr, end = begin + memmap->map[i].size;
        if (memmap->map[i].type == E820_ARM) {
            if (begin < freemem) {
                begin = freemem;
            }
            if (end > KMEMSIZE) {
                end = KMEMSIZE;
            }
            if (begin < end) {
                begin = ROUNDUP(begin, PGSIZE);
                end = ROUNDDOWN(end, PGSIZE);
                if (begin < end) {
                    init_memmap(pa2page(begin), (end - begin) / PGSIZE);
                }
            }
        }
    }
}

static void
enable_paging(void) {
    lcr3(boot_cr3);

    // turn on paging
    uint32_t cr0 = rcr0();
    cr0 |= CR0_PE | CR0_PG | CR0_AM | CR0_WP | CR0_NE | CR0_TS | CR0_EM | CR0_MP;
    cr0 &= ~(CR0_TS | CR0_EM);
    lcr0(cr0);
}

// boot_map_segment - setup&enable the paging mechanism
//  parameters
//   la:   linear address of this memory need to map (after x86 segment map)
//   size: memory size
//   pa:   physical address of this memory
//   perm: permission of this memory
/**
 * @brief 同时设置页目录和页表项，令到线性地址 la ~ la+size 可以页映射到 物理地址 pa ~ pa+size
 * 而且这个映射是连续顺序的
 * @param pgdir 页目录基地址
 * @param la 线性地址
 * @param size 要多大的范围
 * @param pa 物理地址
 * @param perm 页表项的权限
 */
static void
boot_map_segment(pde_t *pgdir, uintptr_t la, size_t size, uintptr_t pa, uint32_t perm) {
    // 先确定线性地址la可以通过页映射到物理地址pa
    // 他们在页/幀内的偏移量必须相等
    assert(PGOFF(la) == PGOFF(pa));
    // --------------
    // --------------
    //
    //  la
    //     |
    //    PGOFF(la) 页内偏移量
    //     |
    // --------------

    // la ~ la+size 占了n页
    size_t n = ROUNDUP(size + PGOFF(la), PGSIZE) / PGSIZE;
    // 线性地址la所在页的基址
    la = ROUNDDOWN(la, PGSIZE);
    // 物理地址pa所在幀的基址
    pa = ROUNDDOWN(pa, PGSIZE);

    // 遍历 la ~ la+size 这n页的页基址
    for (; n > 0; n--, la += PGSIZE, pa += PGSIZE) {
        // 现在变量la是每页的页基址
        pte_t *ptep = get_pte(pgdir, la, 1);
        assert(ptep != NULL);
        *ptep = pa | PTE_P | perm;
    }
}

// boot_alloc_page - allocate one page using pmm->alloc_pages(1)
//  return value: the kernel virtual address of this allocated page
// note: this function is used to get the memory for PDT(Page Directory Table)&PT(Page Table)
/**
 * @brief 申请一页内存
 * @return 内存的线性地址
 */
static void *
boot_alloc_page(void) {
    struct Page *p = alloc_page();
    if (p == NULL) {
        panic("boot_alloc_page failed.\n");
    }
    return page2kva(p);
}

//pmm_init - setup a pmm to manage physical memory, build PDT&PT to setup paging mechanism 
//         - check the correctness of pmm & paging mechanism, print PDT&PT
void
pmm_init(void) {
    //We need to alloc/free the physical memory (granularity is 4KB or other size). 
    //So a framework of physical memory manager (struct pmm_manager)is defined in pmm.h
    //First we should init a physical memory manager(pmm) based on the framework.
    //Then pmm can alloc/free the physical memory. 
    //Now the first_fit/best_fit/worst_fit/buddy_system pmm are available.
    // 1. 初始化物理内存页管理器框架pmm_manager
    init_pmm_manager();

    // detect physical memory space, reserve already used memory,
    // then use pmm->init_memmap to create free page list
    // 2. 建立空闲的page链表，这样就可以分配以页（4KB）为单位的空闲内存了
    // 他把整个可用的物理内存的幀一次性都建立了对应的页Page
    // 注意，这里并不是建立页表项（给CPU看），只是给操作系统自己记账内存用的而已
    page_init();

    //use pmm->check to verify the correctness of the alloc/free function in a pmm
    // 3. 检查物理内存页分配算法
    check_alloc_page();

    // create boot_pgdir, an initial page directory(Page Directory Table, PDT)
    // 申请一页空间，作为临时页目录
    boot_pgdir = boot_alloc_page();
    // 一页空间是4K
    memset(boot_pgdir, 0, PGSIZE);
    // 把临时页目录的物理地址存起来，后面告诉CPU的CR3寄存器，这样子CPU就知道你的页目录在哪了
    boot_cr3 = PADDR(boot_pgdir);

    check_pgdir();

    static_assert(KERNBASE % PTSIZE == 0 && KERNTOP % PTSIZE == 0);

    // recursively insert boot_pgdir in itself
    // to form a virtual page table at virtual address VPT
    // VPT=0xFAC00000 = 1111101011 0... = 0x3EB 0...
    // PDX(VPT)=0x3EB=1003
    // 把页目录的第1003项，写入页目录的地址
    boot_pgdir[PDX(VPT)] = PADDR(boot_pgdir) | PTE_P | PTE_W;

    // map all physical memory to linear memory with base linear addr KERNBASE
    //linear_addr KERNBASE~KERNBASE+KMEMSIZE = phy_addr 0~KMEMSIZE
    //But shouldn't use this map until enable_paging() & gdt_init() finished.
    boot_map_segment(boot_pgdir, KERNBASE, KMEMSIZE, 0, PTE_W);

    //temporary map: 
    //virtual_addr 3G~3G+4M = linear_addr 0~4M = linear_addr 3G~3G+4M = phy_addr 0~4M     
    boot_pgdir[0] = boot_pgdir[PDX(KERNBASE)];

    // 使能分页机制
    enable_paging();

    //reload gdt(third time,the last time) to map all physical memory
    //virtual_addr 0~4G=liear_addr 0~4G
    //then set kernel stack(ss:esp) in TSS, setup TSS in gdt, load TSS
    // 重新设置全局段描述符表
    gdt_init();

    //disable the map of virtual_addr 0~4M
    // 取消临时二级页表
    boot_pgdir[0] = 0;

    //now the basic virtual memory map(see memalyout.h) is established.
    //check the correctness of the basic virtual memory map.
    // 检查页表建立是否正确
    check_boot_pgdir();

    // 通过自映射机制完成页表的打印输出
    print_pgdir();

}

